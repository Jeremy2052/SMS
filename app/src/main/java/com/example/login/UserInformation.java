package com.example.login;

public class UserInformation {

    public String first;
    public String last;
    public String email;
    public String phone;
    public String password;


    public UserInformation(String first, String last, String email, String phone, String password) {
        this.first = first;
        this.last = last;
        this.email = email;
        this.phone = phone;
        this.password = password;
    }
}
