package com.example.login;

public class vendorInformation {
    public String vendor;
    public String address;
    public String email;
    public String phone;
    public String password;
    public String city;
    public String zipcode;

    public vendorInformation(String vendor, String address, String email, String phone, String password, String city, String zipcode) {
        this.vendor = vendor;
        this.address = address;
        this.email = email;
        this.phone = phone;
        this.password = password;
        this.city = city;
        this.zipcode = zipcode;
    }
}
